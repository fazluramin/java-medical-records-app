    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (id_perawat.getText().equals("") || nama_perawat.getText().equals("") || umur.getText().equals("") ||
            alamat.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Lengkapi Data!");
        } 
        else {
            String jk=null;
            if(pria.isSelected()){
                jk="Pria";
            }
            else if(wanita.isSelected()){
                jk="Wanita";
            }
            
            String[] kolom = {"id_perawat", "nama_perawat", "jenis_kelamin", "umur", "agama", "alamat"};
            String[] isi = {id_perawat.getText(), 
                            nama_perawat.getText(),
                            jk,
                            umur.getText(),
                            (String) agama.getSelectedItem(),
                            alamat.getText()};
            System.out.println(koneksi.queryInsert("tb_Perawat", kolom, isi));
            table();
            refresh();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

